//---> TIME AND SPACE COMPLEXITY


//-> Time Complexity  -> It is the amount of time taken by an algorithm to run, as a function of length of the input.
/*
Notations :-

    1) Big O notation -> upper bound(best case)
    2) Theta notation -> average case
    3) Omega notation -> lower bound(worst case)


Some ex->
O(1)     -->  Constant Time 
O(n)     -->  Linear Time
O(log n) -->  Logarithmic Time
O(n^2)   -->  Quadratic Time
O(n^3)   -->  Cubic time


Complexity order(Increasing down)

O(1)        -> Least Complexity
O(logn)
O(n)
O(nlogn)
O(n^2)
O(n^3)
O(n^3)
O(2^n)
O(n!)       -> Highest Complexity

*/

